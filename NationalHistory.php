			<center><b>Growth and the Future</b></center>

			<hr /></td>
		</tr>
		<tr>
			<td>
			<p align="justify">Rapid expansion followed throughout the 1990s. Chapters were chartered across the nation through interest groups such as those at the State University of New York, Buffalo and the University of Michigan, Ann-Arbor, leading to the fraternity having a notable presence in all major regions of the country. After becoming a California non-profit organization and incorporating in 1995, the fraternity changed its official name to Lambda Phi Epsilon National Fraternity, Inc. The next decade brought the fraternity international status with the formation of a chapter at the University of Toronto in 2004, making it the first Asian interest fraternity established in Canada. The Northeastern University chapter chartered on April 12, 2009.</p>

			<p align="justify">With the organization&#39;s rapid expansion across North America thus far, the national officer corps has begun to revitalize and refocus the efforts of individual chapters and the fraternity as a whole to better reflect its size and influence in the Asian community through academics, leadership, brotherhood, and service.</p>
